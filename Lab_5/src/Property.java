public class Property {
    private int id;
    private double area;
    private String city;
    private char type;
    private int year;
    private int price;
    private String owner;

    public Property() {
    }

    public Property(int id, double area, String city, char type, int year, int price, String owner) {
        this.id = id;
        this.area = area;
        this.city = city;
        this.type = type;
        this.year = year;
        this.price = price;
        this.owner = owner;
    }

}

